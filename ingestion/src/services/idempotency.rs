#[cfg(test)]
mod tests {
    use super::*;

    // --- event_id format ---

    #[test]
    fn event_id_has_evt_prefix() {
        // nanoid-generated IDs must always carry the `evt_` prefix so that
        // downstream systems (worker, logs, dashboards) can recognize them.
        let event_id = format!("evt_{}", nanoid::nanoid!(21));
        assert!(
            event_id.starts_with("evt_"),
            "event_id must start with 'evt_' — got: {event_id}"
        );
    }

    #[test]
    fn event_id_minimum_length() {
        // `evt_` (4) + at least 8 random chars = 12 minimum.
        let event_id = format!("evt_{}", nanoid::nanoid!(21));
        assert!(
            event_id.len() >= 12,
            "event_id too short: {event_id}"
        );
    }

    #[test]
    fn idempotency_record_pk_format() {
        let pk = IdempotencyRecord::pk_for("req_abc123");
        assert_eq!(pk, "IDEM#req_abc123");
    }

    #[test]
    fn idempotency_record_ttl_is_24h_after_created_at() {
        let created_at = 1_707_840_000_i64;
        let record = IdempotencyRecord {
            pk: IdempotencyRecord::pk_for("req_ttl_test"),
            event_id: "evt_ttl_test".to_string(),
            created_at,
            ttl: created_at + 86_400,
        };
        assert_eq!(record.ttl - record.created_at, 86_400);
    }

    #[test]
    fn idempotency_record_serializes_for_dynamo() {
        // Ensures serde_dynamo can convert the struct without panicking.
        let record = IdempotencyRecord {
            pk: IdempotencyRecord::pk_for("req_serial_test"),
            event_id: "evt_serial_test".to_string(),
            created_at: 1_707_840_000,
            ttl: 1_707_840_000 + 86_400,
        };
        let item = serde_dynamo::aws_sdk_dynamodb_1::to_item(&record);
        assert!(item.is_ok(), "serialization should succeed: {:?}", item.err());
    }
}
